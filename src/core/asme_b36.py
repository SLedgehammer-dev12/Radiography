def get_pipe_od(nps_key, standard="B36.10"):
    if standard == "B36.19":
        d = ASME_B36_19_PIPES
    else:
        d = ASME_B36_10_PIPES
    entry = d.get(nps_key)
    if entry is None:
        return None
    return entry[0]

def get_pipe_schedules(nps_key, standard="B36.10"):
    if standard == "B36.19":
        d = ASME_B36_19_PIPES
    else:
        d = ASME_B36_10_PIPES
    entry = d.get(nps_key)
    if entry is None:
        return []
    return list(entry[1])

def find_nps_by_od(od_mm, tolerance=0.5, standard="B36.10"):
    if standard == "B36.19":
        d = ASME_B36_19_PIPES
    else:
        d = ASME_B36_10_PIPES
    for key, (od, _) in d.items():
        if abs(od - od_mm) <= tolerance:
            return key
    return None

def get_default_schedule(nps_key, standard="B36.10"):
    schedules = get_pipe_schedules(nps_key, standard)
    if not schedules:
        return None
    for wall, label in schedules:
        if "STD" in label or "SCH 40" in label:
            return (wall, label)
    return schedules[0]

