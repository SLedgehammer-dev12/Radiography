    def set_language(self, lang):
        if lang in self.translations:
            self.language = lang

    def get(self, key, *args):
        lang_dict = self.translations.get(self.language, self.translations["tr"])
        text = lang_dict.get(key, key)
        if args:
            try:
                return text.format(*args)
            except Exception:
                return text
        return text
